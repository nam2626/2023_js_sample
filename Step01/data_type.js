//데이터 타입
//1. Number (숫자 - 정수, 실수 포함)
//2. String (문자열)
//3. Boolean (논리값 - true, false)
//4. undefined (값이 할당되지 않은 상태)
//5. null (값이 없음)

//1. Number
var a = 10;
var b = 10.5;
console.log(typeof a,typeof b);
//2. String
var str1 = 'Hello';
var str2 = "Hello";
console.log(typeof str1, typeof str2);
//3. Boolean
var flag1 = true;
var flag2 = false;
console.log(typeof flag1, typeof flag2);  
//4. undefined
var num;
console.log(num, typeof num);
//5. null
var n = null;
console.log(n, typeof n);